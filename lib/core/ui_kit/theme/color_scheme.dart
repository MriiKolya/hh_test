import 'dart:ui';

abstract class PrimaryColor {
  static Color green = const Color(0xff22BB9C);
  static Color white = const Color(0xffFFFFFF);
}

abstract class SecondaryColor {
  static Color grey = const Color(0xffBDBDBD);
  static Color black = const Color(0xff171725);
  static Color backgroundGrey = const Color(0xffF5F5F5);
  static Color darkGrey = const Color(0xff515150);
}

abstract class TextColor {
  static Color black = const Color(0xff171725);
  static Color white = const Color(0xffFFFFFF);
}
